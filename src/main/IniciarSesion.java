    private void btnExaminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExaminarActionPerformed

        JFileChooser chooser=new JFileChooser(".");
        FileNameExtensionFilter filter=new FileNameExtensionFilter("Archivos DAT","dat");
        chooser.setFileFilter(filter);
        chooser.setCurrentDirectory(new java.io.File("."));
        chooser.setDialogTitle("Buscar");

        chooser.setAcceptAllFileFilterUsed(false);
        
        if (chooser.showOpenDialog(null)==JFileChooser.APPROVE_OPTION) {
            
            txtExaminar.setText(String.valueOf(chooser.getCurrentDirectory())+"/"+String.valueOf(chooser.getSelectedFile().getName()));
        }
    }//GEN-LAST:event_btnExaminarActionPerformed

